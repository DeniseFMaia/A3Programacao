    public void carregaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) this.jTEmprestimos.getModel();
        modelo.setNumRows(0); //Posiciona na primeira linha da tabela
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        //sdf.format(date);
        Integer filtro = jCFiltroExibir.getSelectedIndex();
        System.out.println("filtro " + filtro);
//Carrega a lista de objetos aluno
        ArrayList<Emprestimo> minhalista = objetoemprestimo.getMinhaLista();
        for (Emprestimo a : minhalista) {
            String dtdev = null;
            try {
                dtdev = sdf.format(a.getDataDevolucao());
            } catch (Exception e) {
            }
            if ((filtro == 0)
                    || (filtro == 1 && dtdev == null)
                    || (filtro == 2 && dtdev != null)) {
                
                modelo.addRow(new Object[]{
                    a.getId(),
                    objetoamigo.getNomeById(a.getIdAmigo()),
                    objetoferramenta.getNomeById(a.getIdFerramenta()),
                    sdf.format(a.getDataEmprestimo()),
                    sdf.format(a.getDataPrevDevolucao()),
                    dtdev,
                    a.getStatus()
                });
            }
            
        }
    }

